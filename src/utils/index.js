export function createUniqueString() {
  const timestamp = +new Date() + ''
  const randomNum = parseInt((1 + Math.random()) * 65536) + ''
  return (+(randomNum + timestamp)).toString(32)
}

/**
 * Check if an element has a class
 * @param {HTMLElement} elm
 * @param {string} cls
 * @returns {boolean}
 */
export function hasClass(ele, cls) {
  return !!ele.className.match(new RegExp('(\\s|^)' + cls + '(\\s|$)'))
}

/**
 * Add class to element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function addClass(ele, cls) {
  if (!hasClass(ele, cls)) ele.className += ' ' + cls
}

/**
 * Remove class from element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function removeClass(ele, cls) {
  if (hasClass(ele, cls)) {
    const reg = new RegExp('(\\s|^)' + cls + '(\\s|$)')
    ele.className = ele.className.replace(reg, ' ')
  }
}

/**
 * @Descripttion: 处理复杂判断的简单写法
 * @param {action} Object
 * let multi = new MultiCondResult({
    'STRING|COMBO|BOOLEAN': 'TextColumn',
    '/^(IMAGE)$/': 'ImageColumn',
    '/^.*$/': 'aaaaaaaaaaa',
  })
  multi.getValue('STRING') // TextColumn
 */
export class MultiCondResult {
  constructor(action) {
    let actionArr = []
    for (let key in action) {
      try {
        // 正则
        actionArr.push([this.evil(key), action[key]])
      } catch {
        // 去掉所有空格
        let newKey = key === 'default' ? '.*' : key
        let trimKey = newKey.replace(/\s/g, '')
        actionArr.push([new RegExp(`^(${trimKey})$`), action[key]])
        // actionArr.push([new RegExp(`^(${key === 'default' ? '.*' : key})$`), action[key]])
      }
    }
    this.mapAction = new Map(actionArr)
  }
  // 计算表达式的值
  evil(fn) {
    var Fn = Function // 一个变量指向Function，防止有些前端编译工具报错
    return new Fn('return ' + fn)()
  }
  // 传key获取value
  getValue(name) {
    let arr = [...this.mapAction].filter(([key, value]) => key.test(name))
    return arr[0][1].constructor === Function ? arr[0][1].call(this) : arr[0][1]
  }
}
